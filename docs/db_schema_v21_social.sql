-- ────────────────────────────────────────────
-- 4. DIRECT MESSAGES
-- ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS direct_messages (
  id          UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  sender_id   UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  receiver_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  content     TEXT NOT NULL CHECK (length(trim(content)) > 0 AND length(content) <= 1000),
  read_at     TIMESTAMP WITH TIME ZONE,
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_dm_sender   ON direct_messages(sender_id, created_at DESC);
CREATE INDEX IF NOT EXISTS idx_dm_receiver ON direct_messages(receiver_id, created_at DESC);

ALTER TABLE direct_messages ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "dm_read_own"   ON direct_messages;
DROP POLICY IF EXISTS "dm_send_own"   ON direct_messages;
DROP POLICY IF EXISTS "dm_delete_own" ON direct_messages;
CREATE POLICY "dm_read_own"   ON direct_messages FOR SELECT USING (auth.uid() = sender_id OR auth.uid() = receiver_id);
CREATE POLICY "dm_send_own"   ON direct_messages FOR INSERT WITH CHECK (auth.uid() = sender_id);
CREATE POLICY "dm_delete_own" ON direct_messages FOR DELETE USING (auth.uid() = sender_id);

ALTER PUBLICATION supabase_realtime ADD TABLE direct_messages;

-- ────────────────────────────────────────────
-- 5. MAINTENANCE LOGS (bakım takvimi)
-- ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS maintenance_logs (
  id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id       UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  category      TEXT NOT NULL DEFAULT 'Genel',
  title         TEXT NOT NULL,
  date          DATE NOT NULL,
  cost          NUMERIC(10,2),
  odometer_km   INTEGER,
  notes         TEXT,
  next_due_date DATE,
  created_at    TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_maintenance_user ON maintenance_logs(user_id, date DESC);

ALTER TABLE maintenance_logs ENABLE ROW LEVEL SECURITY;
