-- Karavan Komşusu — v21: Social layer (follows, spot reviews, check-ins, DM, maintenance)

-- ────────────────────────────────────────────
-- 1. FOLLOWS
-- ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS follows (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  follower_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  following_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  created_at  TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(follower_id, following_id),
  CHECK (follower_id != following_id)
);

CREATE INDEX IF NOT EXISTS idx_follows_follower  ON follows(follower_id);
CREATE INDEX IF NOT EXISTS idx_follows_following ON follows(following_id);

ALTER TABLE follows ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "follows_read_all"    ON follows;
DROP POLICY IF EXISTS "follows_insert_own"  ON follows;
DROP POLICY IF EXISTS "follows_delete_own"  ON follows;
CREATE POLICY "follows_read_all"   ON follows FOR SELECT USING (true);
CREATE POLICY "follows_insert_own" ON follows FOR INSERT WITH CHECK (auth.uid() = follower_id);
CREATE POLICY "follows_delete_own" ON follows FOR DELETE USING (auth.uid() = follower_id);

-- ────────────────────────────────────────────
-- 2. SPOT REVIEWS (yıldız + yorum)
-- ────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS spot_reviews (
  id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id  UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  spot_id  BIGINT NOT NULL,
  rating   INTEGER NOT NULL CHECK (rating BETWEEN 1 AND 5),
  comment  TEXT CHECK (length(comment) <= 500),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(user_id, spot_id)
);

CREATE INDEX IF NOT EXISTS idx_spot_reviews_spot ON spot_reviews(spot_id);

ALTER TABLE spot_reviews ENABLE ROW LEVEL SECURITY;
DROP POLICY IF EXISTS "spot_reviews_read_all"   ON spot_reviews;
DROP POLICY IF EXISTS "spot_reviews_insert_own" ON spot_reviews;
DROP POLICY IF EXISTS "spot_reviews_update_own" ON spot_reviews;
DROP POLICY IF EXISTS "spot_reviews_delete_own" ON spot_reviews;
CREATE POLICY "spot_reviews_read_all"   ON spot_reviews FOR SELECT USING (true);
CREATE POLICY "spot_reviews_insert_own" ON spot_reviews FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "spot_reviews_update_own" ON spot_reviews FOR UPDATE USING (auth.uid() = user_id);
CREATE POLICY "spot_reviews_delete_own" ON spot_reviews FOR DELETE USING (auth.uid() = user_id);

