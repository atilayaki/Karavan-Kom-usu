-- Karavan Komşusu — v19: City stickers / regional badge collection
-- Each Turkish region has a themed sticker. Users earn it on first post/item from that city.

CREATE TABLE IF NOT EXISTS city_stickers (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  city_slug TEXT UNIQUE NOT NULL,
  city_name TEXT NOT NULL,
  region TEXT NOT NULL,
  emoji TEXT NOT NULL,
  description TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS user_city_stickers (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id UUID NOT NULL REFERENCES profiles(id) ON DELETE CASCADE,
  sticker_id BIGINT NOT NULL REFERENCES city_stickers(id) ON DELETE CASCADE,
  earned_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  UNIQUE(user_id, sticker_id)
);

CREATE INDEX IF NOT EXISTS idx_user_city_stickers_user ON user_city_stickers(user_id);

