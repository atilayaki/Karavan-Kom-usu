-- Helper: extract candidate city slug from a free-text location string
CREATE OR REPLACE FUNCTION match_city_sticker(loc_text TEXT)
RETURNS BIGINT AS $$
DECLARE
  found_id BIGINT;
  needle TEXT;
BEGIN
  IF loc_text IS NULL OR length(trim(loc_text)) = 0 THEN RETURN NULL; END IF;
  needle := lower(unaccent_ish(loc_text));
  SELECT id INTO found_id FROM city_stickers
  WHERE position(city_slug IN needle) > 0
     OR position(lower(unaccent_ish(city_name)) IN needle) > 0
  LIMIT 1;
  RETURN found_id;
END;
$$ LANGUAGE plpgsql;

-- Lightweight Turkish-aware lower (no extension dependency)
CREATE OR REPLACE FUNCTION unaccent_ish(t TEXT)
RETURNS TEXT AS $$
BEGIN
  RETURN translate(lower(t),
    'çğıöşüâîû',
    'cgiosuaiu');
END;
$$ LANGUAGE plpgsql IMMUTABLE;

-- Trigger: award sticker on post insert
CREATE OR REPLACE FUNCTION award_city_sticker_post()
RETURNS TRIGGER AS $$
DECLARE
  s_id BIGINT;
BEGIN
  IF NEW.location_name IS NULL THEN RETURN NEW; END IF;
  s_id := match_city_sticker(NEW.location_name);
  IF s_id IS NOT NULL THEN
    INSERT INTO user_city_stickers (user_id, sticker_id)
    VALUES (NEW.user_id, s_id)
    ON CONFLICT (user_id, sticker_id) DO NOTHING;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_award_sticker_post ON posts;
CREATE TRIGGER trg_award_sticker_post
AFTER INSERT ON posts
FOR EACH ROW EXECUTE FUNCTION award_city_sticker_post();

