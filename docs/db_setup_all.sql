DO $$ BEGIN
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='objects' AND policyname='Public Access') THEN
    EXECUTE $POL$ CREATE POLICY "Public Access" ON storage.objects FOR SELECT USING (bucket_id = 'karavan_images') $POL$;
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='objects' AND policyname='Authenticated upload') THEN
    EXECUTE $POL$ CREATE POLICY "Authenticated upload" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'karavan_images' AND auth.role() = 'authenticated') $POL$;
  END IF;
  IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE tablename='objects' AND policyname='Owner delete') THEN
    EXECUTE $POL$ CREATE POLICY "Owner delete" ON storage.objects FOR DELETE USING (bucket_id = 'karavan_images' AND auth.uid() = owner) $POL$;
  END IF;
END $$;

